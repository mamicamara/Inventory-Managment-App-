package com.gyp1.inventorymgtapp;

/**
 * An enumerated type for specifying whether a user input action is
 * adding or editing data (during Part or Product input).
 *
 * @author Mami Camara
 * @version April 2023
 */
public enum UserAction {
    /**
     * Represents the user action of entering new information.
     */
    ADD,
    /**
     * Represents the user action of editing existing information.
     */
    MODIFY
}
